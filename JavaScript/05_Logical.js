// Logical Operators
// && (AND)
// || (OR)
// ! (NOT)

let isLoggedin = true;
let isPaid = false;

console.log(isLoggedin && isPaid); // false
console.log(isLoggedin || isPaid); // true
console.log(!isLoggedin); // false
console.log(!isPaid); // true
console.log(!isLoggedin && isPaid); // false
console.log(!isLoggedin || isPaid); // true
console.log(isLoggedin && !isPaid); // true
